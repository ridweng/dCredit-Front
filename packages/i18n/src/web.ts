export function translateWeb(
  locale: Locale,
  key: WebTranslationKey,
  params?: TranslationParams,
): string {
  let template =
    webTranslations[locale]?.[key] ??
    webTranslations.en[key] ??
    key;

  if (params) {
    for (const [paramKey, paramValue] of Object.entries(params)) {
      template = template.split(`{${paramKey}}`).join(String(paramValue));
    }
  }

  return template;
}
